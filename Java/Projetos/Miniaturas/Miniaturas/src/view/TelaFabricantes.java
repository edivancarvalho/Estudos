    private void consultarFabricantes(List<Fabricantes> lista) throws Exception {
        DefaultTableModel modelo = (DefaultTableModel) tableFabricante.getModel();
        modelo.setNumRows(0);
        for (int pos = 0; pos < lista.size(); pos++) {
            String[] linha = new String[2];
            Fabricantes aux = lista.get(pos);
            linha[0] = aux.getIden() + "";
            linha[1] = aux.getNome() + "";
            modelo.addRow(linha);

        }
    }

    private void limpaCampos() {
        txtFabId.setText("");
        txtFabNome.setText("");
    }

    private void preencheCampos(int id) throws Exception {

        try {

            if (id > 0) {

                fab = fabBll.consultaFabricantesPorId(id);
                txtFabId.setText(id + "");
                txtFabNome.setText(fab.getNome());
                btnFabSalvar.setLabel("Editar");
            } else {
                btnFabSalvar.setLabel("Salvar");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

