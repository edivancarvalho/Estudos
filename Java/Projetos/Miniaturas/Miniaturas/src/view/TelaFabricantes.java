    private void tableFabricanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableFabricanteMouseClicked
        int linha = tableFabricante.getSelectedRow();
        Integer codigo = Integer.parseInt(tableFabricante.getValueAt(linha, 0).toString());
        try {
            preencheCampos((int) codigo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher campo");
        }

    }//GEN-LAST:event_tableFabricanteMouseClicked


