    private void btnFabSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFabSalvarActionPerformed
        try {
            fab.setNome(txtFabNome.getText());
            if (btnFabSalvar.getLabel().equals("Salvar")) {
                fabBll.adicionarFabricante(fab);

            } else {
                fabBll.alterarFabricante(fab);

            }
            consultarFabricantes(fabBll.consultarFabricantes());
            limpaCampos();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnFabSalvarActionPerformed

    private void btnFabExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFabExcluirActionPerformed
        try {
            fabBll.removerFabricante(fabBll.consultaFabricantesPorId(fab.getIden()));
            limpaCampos();
            consultarFabricantes(fabBll.consultarFabricantes());
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_btnFabExcluirActionPerformed

